
CREATE TABLE IF NOT EXISTS Users (
user_id varchar(20) NOT NULL,
email varchar(50) DEFAULT NULL,
first_name varchar(30) DEFAULT NULL,
last_name varchar(30) DEFAULT NULL,
phone_num varchar(10) DEFAULT NULL,
PRIMARY KEY (user_id)
) ;

CREATE TABLE IF NOT EXISTS Student (
user_id varchar(20) NOT NULL,
major varchar(50) DEFAULT NULL,
PRIMARY KEY (user_id),
CONSTRAINT student_ibfk_1 FOREIGN KEY (user_id) REFERENCES Users (user_id)
ON DELETE CASCADE
) ;

CREATE TABLE IF NOT EXISTS Course (
dept varchar(20) NOT NULL,
cno varchar(20) NOT NULL,
title varchar(100) DEFAULT NULL,
level varchar(100) NOT NULL,
PRIMARY KEY (dept,cno)
);

--DROP TABLE IF EXISTS Class;
CREATE TABLE IF NOT EXISTS Class (
class_no varchar(20) NOT NULL,
dept varchar(20) NOT NULL,
cno varchar(20) NOT NULL,
start_time date DEFAULT NULL,
end_time date DEFAULT NULL,
max_students int DEFAULT NULL,
term varchar(35) NOT NULL,
year int DEFAULT NULL,
PRIMARY KEY (class_no,dept,cno),
CONSTRAINT class_ibfk_1 FOREIGN KEY (dept, cno) REFERENCES Course
(dept, cno) ON DELETE CASCADE
);

--DROP TABLE IF EXISTS Assignment;
CREATE TABLE IF NOT EXISTS Assignment (
assign_id varchar(15) NOT NULL,
class_no varchar(20) NOT NULL,
dept varchar(20) NOT NULL,
cno varchar(20) NOT NULL,
name varchar(100) DEFAULT NULL,
deadline timestamp DEFAULT NULL,
PRIMARY KEY (assign_id),
CONSTRAINT assignment_ibfk_1 FOREIGN KEY (class_no, dept, cno) REFERENCES
Class (class_no, dept, cno)
);

CREATE TABLE IF NOT EXISTS Instructor (
user_id varchar(20) NOT NULL,
title varchar(50) DEFAULT NULL,
PRIMARY KEY (user_id),
CONSTRAINT instructor_ibfk_1 FOREIGN KEY (user_id) REFERENCES Users ON DELETE CASCADE
);

--DROP TABLE IF EXISTS Does;
CREATE TABLE IF NOT EXISTS Does (
user_id varchar(20) NOT NULL,
assign_id varchar(15) NOT NULL,
grade decimal(4,2) DEFAULT NULL,
PRIMARY KEY (user_id,assign_id),
CONSTRAINT does_ibfk_1 FOREIGN KEY (user_id) REFERENCES Student
(user_id),
CONSTRAINT does_ibfk_2 FOREIGN KEY (assign_id) REFERENCES Assignment
(assign_id)
) ;

CREATE TABLE IF NOT EXISTS Phone (
user_id varchar(20) NOT NULL,
type varchar(100) NOT NULL,
number varchar(20) NOT NULL,
PRIMARY KEY (user_id,type,number),
CONSTRAINT phone_ibfk_1 FOREIGN KEY (user_id) REFERENCES Users (user_id)
);

CREATE TABLE IF NOT EXISTS Takes (
user_id varchar(20) NOT NULL,
class_no varchar(20) NOT NULL,
dept varchar(20) NOT NULL,
cno varchar(20) NOT NULL,
grade decimal(4,2) DEFAULT NULL,
PRIMARY KEY (user_id,class_no,dept,cno),
CONSTRAINT takes_ibfk_1 FOREIGN KEY (user_id) REFERENCES Student
(user_id),
CONSTRAINT takes_ibfk_2 FOREIGN KEY (class_no, dept, cno) REFERENCES Class (class_no, dept, cno)
);

